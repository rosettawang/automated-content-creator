@pytest.fixture()
def make_clip(conn):
    """Insert a clip row. present=True drops a real playable file in MEDIA_DIR and sets
    media_path; present=False leaves a catalog-only 'ghost' (no file on disk)."""
    def _make(file_stem, present=True, ext=".mp4", **cols):
        media_path = None
        if present:
            src = _make_sample(ext)
            dest = _MEDIA / f"{file_stem}{ext}"
            shutil.copy(src, dest)
            media_path = str(dest)
        fields = {
            "file_stem": file_stem,
            "duration_s": cols.pop("duration_s", 1.0),
            "kind": cols.pop("kind", "video"),
            "content_hash": cols.pop("content_hash", uuid.uuid4().hex),
            "media_path": media_path,
            "media_status": "present" if present else "absent",
            "description": cols.pop("description", ""),
            "category": cols.pop("category", ""),
            "tags": cols.pop("tags", ""),
            "context": cols.pop("context", ""),
        }
        fields.update(cols)
        keys = ", ".join(fields)
        qs = ", ".join("?" for _ in fields)
        cur = conn.execute(f"INSERT INTO clips ({keys}) VALUES ({qs})", tuple(fields.values()))
        conn.commit()
        return cur.lastrowid
    return _make


@pytest.fixture()
def mock_ai(monkeypatch):
    """Patch the two generation seams (imported into blueprints.edits via `import *`).
    The fakes build selections from the pool they're handed, so returned clip_ids are
    always valid + we can capture which clips the pool exposed. Returns the call log."""
    import blueprints.edits as edits

    calls = {"generate": [], "revise": []}

    def fake_generate(prompt, clips):
        calls["generate"].append({"prompt": prompt, "clips": clips})
        sels = [ClipSelection(clip_id=c["id"], in_point=0.0, out_point=1.0, reason="test")
                for c in clips[:2]]
        return RoughCutPlan(concept="Test concept", selections=sels, aspect=None)

    def fake_revise(instruction, current_timeline, clips):
        calls["revise"].append({"instruction": instruction, "current": current_timeline, "clips": clips})
        # Return the whole pool so the revised timeline visibly differs from a
        # single-item starting timeline (lets the undo test assert a real change).
        sels = [ClipSelection(clip_id=c["id"], in_point=0.0, out_point=1.0, reason="test")
                for c in (clips or [])]
        return EditChatResult(reply="Applied your change.", selections=sels, aspect=None)

    monkeypatch.setattr(edits, "generate_rough_cut", fake_generate)
    monkeypatch.setattr(edits, "revise_edit", fake_revise)
    return calls
