def _pytest_sessionfinish_cleanup():
    shutil.rmtree(_TMP, ignore_errors=True)


def pytest_sessionfinish(session, exitstatus):
    _pytest_sessionfinish_cleanup()


# --- tiny real media, generated once and copied per-test (fast + isolated) ---
def _make_sample(ext: str) -> Path:
    """Generate a 1s 320x240 H.264 clip in the session cache (once per extension)."""
    dest = _CACHE / f"sample{ext}"
    if dest.exists():
        return dest
    subprocess.run(
        ["ffmpeg", "-y", "-f", "lavfi",
         "-i", "testsrc=duration=1:size=320x240:rate=15",
         "-c:v", "libx264", "-pix_fmt", "yuv420p", "-t", "1", str(dest)],
        check=True, capture_output=True,
    )
    return dest


